# -----------------------------------------------------------------------------
# 질문 → 표(std_id) 라우팅의 결정적 단위 검증 (LLM 불필요).
#   비슷한 두 표('친환경제품 확대 희망' vs '환경표지 인증제품 확대 희망')를
#   결정적 키워드로 가르는지, 무관한 질문엔 라우팅하지 않는지(None) 확인한다.
# -----------------------------------------------------------------------------

import sys
from pathlib import Path

PROJECT_ROOT = Path(__file__).resolve().parents[1]
if str(PROJECT_ROOT) not in sys.path:
    sys.path.insert(0, str(PROJECT_ROOT))

import pytest

from rag.retrieval.routing import route


@pytest.mark.parametrize("query, expected", [
    # '친환경제품'(환경표지/녹색제품/인증 언급 없이) + 확대 → 표 3-60(친환경제품)
    ("2023년에 확대되길 바라는 친환경제품 1위는?", "친환경제품_확대희망품목"),
    ("친환경제품 확대 희망 품목 알려줘", "친환경제품_확대희망품목"),
    # 환경표지/녹색제품/인증 + 확대 → 환경표지 인증제품 표
    ("2023년 환경표지 인증제품 확대 희망 품목 1위는?", "환경표지_확대희망품목"),
    ("녹색제품 중 확대되길 바라는 품목은?", "환경표지_확대희망품목"),
    ("2025년에 확대되길 바라는 환경표지 인증제품 1위는?", "환경표지_확대희망품목"),
    # 둘 다 언급되면 더 구체적인 환경표지 규칙이 먼저(순서)
    ("환경표지 인증제품(친환경제품) 확대 희망 1위?", "환경표지_확대희망품목"),
    # '확대' 주제가 아니면 라우팅 안 함
    ("2023년 녹색제품 인지도는?", None),
    ("2024년 그린카드 인지도는?", None),
    ("친환경제품 구매 경험은?", None),
])
def test_route(query, expected):
    assert route(query) == expected
